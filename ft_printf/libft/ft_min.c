#include "libft.h"

int	ft_min(int *ar, int size)
{
	int i;
	int min;

	min = ar[0];
	i = 1;
	while (i < size)
	{
		if (ar[i] < min)
			min = ar[i];
		i++;
	}
	return (min);
}
